package ed.rdr2.model;

/**
 * Tipos de evento dinámico del mundo del juego.
 *
 * @author Daniel Palacios Alonso
 */
public enum EventType {
    /** Emboscada de las autoridades o bandas rivales. */
    AMBUSH,
    /** Encuentro fortuito con un personaje. */
    ENCOUNTER,
    /** Urgencia interna en el campamento. */
    CAMP_EMERGENCY,
    /** Descubrimiento de un nuevo lugar o recurso. */
    DISCOVERY
}
